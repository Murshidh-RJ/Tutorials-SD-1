'''
Task 11: Grocery Bill Estimator: 

Write a script that takes the price of three items and calculates the total cost.

'''
# Input prices of three items
item1 = float(input("Enter the price of item 1: "))
item2 = float(input("Enter the price of item 2: "))
item3 = float(input("Enter the price of item 3: "))

# Calculate the total cost
total_cost = item1 + item2 + item3

# Output the total cost
print("Total cost of the items is:", total_cost)
